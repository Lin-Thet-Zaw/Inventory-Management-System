    public void loadCustomerData(){
        try{
           conn = DriverManager.getConnection("jdbc:derby://localhost:1527/InventorySYS", "root", "root");
           smt = conn.createStatement();
           rs = smt.executeQuery("select * from CUSTOMER");
           customerTable.setModel(DbUtils.resultSetToTableModel(rs));
           conn.close();
        }catch(SQLException e){
        JOptionPane.showMessageDialog(this, "Error while getting customer data" + e.getMessage() );
        }
    }
    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        if(customerId.getText().isEmpty() || customerName.getText().isEmpty() || customerPhone.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter data for create new customer");
        }else{
            try{
                conn = DriverManager.getConnection("jdbc:derby://localhost:1527/InventorySYS", "root", "root");
                PreparedStatement addData = conn.prepareStatement("insert into CUSTOMER values(?,?,?)");
                addData.setInt(1, Integer.parseInt(customerId.getText()));
                addData.setString(2, customerName.getText());
                addData.setString(3, customerPhone.getText());
                addData.executeUpdate();
                JOptionPane.showMessageDialog(this, "Successfully created new customer data");
                conn.close();
                loadCustomerData();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Error while creating new customer data");
            }
        }
    }//GEN-LAST:event_addMouseClicked

    private void customerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)customerTable.getModel();
        int selectedIndex = customerTable.getSelectedRow();
        customerId.setText(model.getValueAt(selectedIndex, 0).toString());
        customerName.setText(model.getValueAt(selectedIndex, 1).toString());
        customerPhone.setText(model.getValueAt(selectedIndex, 2).toString());
        try {
            Statement smt1 = null;
            Statement smt2 = null;
            ResultSet rs1 = null;
            ResultSet rs2 = null;

            conn = DriverManager.getConnection("jdbc:derby://localhost:1527/InventorySYS", "root", "root");

            smt1 = conn.createStatement();
            smt2 = conn.createStatement();

            // Query to count orders
            rs1 = smt1.executeQuery("select count(*) from root.ORDER_TABLE where CUSTOMER_NAME='" + model.getValueAt(selectedIndex, 1).toString() + "'");
            if (rs1.next()) {
                orderNumber.setText("" + rs1.getInt(1));
            }

            // Query to sum amounts
            rs2 = smt2.executeQuery("select sum(AMOUNT) from root.ORDER_TABLE where CUSTOMER_NAME='" + model.getValueAt(selectedIndex, 1).toString() + "'");
            if (rs2.next()) {
                orderAmount.setText("$ " + rs2.getInt(1));
            }

            // Close resources
            if (rs1 != null) rs1.close();
            if (rs2 != null) rs2.close();
            if (smt1 != null) smt1.close();
            if (smt2 != null) smt2.close();

            conn.close();
            loadCustomerData();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_customerTableMouseClicked

