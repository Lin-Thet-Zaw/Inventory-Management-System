    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        if(customerId.getText().isEmpty() || customerName.getText().isEmpty() || customerPhone.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Please selected data for  update customer data");
        }else{
            try{
                conn = DriverManager.getConnection("jdbc:derby://localhost:1527/InventorySYS", "root", "root");
                String updateQuery = "UPDATE root.CUSTOMER SET CUSTOMER_NAME = '" + customerName.getText() + "', " +
                "CUSTOMER_PHONE = '" + customerPhone.getText()+ "' " +
                "WHERE CUSTOMER_ID = " + customerId.getText(); // Removed single quotes
                Statement update = conn.createStatement();
                update.executeUpdate(updateQuery);
                JOptionPane.showMessageDialog(this, "Successfully updated customer data");
                conn.close();
                loadCustomerData();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Error while updating data for customer data " + e.getMessage());
            }
        }
    }//GEN-LAST:event_updateMouseClicked

